function setLanguage(lang) {
  // We use English as default for missing languages. 
  // We handle layout translation:
  const t = translations[lang] || translations['en'];

  document.title = t.title || translations['en'].title;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key]) {
      el.textContent = t[key];
    } else if (translations['en'][key]) {
      el.textContent = translations['en'][key];
    }
  });

  // Hot swap logic for images
  // For 'ru' we attach _ru.png, else _en.png
  // To add new images, place them in assets/images/ with '_en.png' and '_ru.png' suffixes (e.g., Screen2_en.png, Screen2_ru.png)
  const imgSuffix = (lang === 'ru') ? '_ru.png' : '_en.png';
  document.querySelectorAll('img[data-i18n-src]').forEach(img => {
    const baseSrc = img.getAttribute('data-i18n-src');
    img.src = `${baseSrc}${imgSuffix}`;
  });

  const docs = pdfLinks[lang === 'ru' ? 'ru' : 'en'];
  document.getElementById('link-terms').href = docs.terms;
  document.getElementById('link-privacy').href = docs.privacy;
  document.getElementById('link-processing').href = docs.processing;
}

document.addEventListener('DOMContentLoaded', () => {
  const select = document.getElementById('lang-select');
  const userLang = navigator.language.slice(0, 2);
  const defaultLang = translations[userLang] ? userLang : 'en';

  select.value = defaultLang;
  setLanguage(defaultLang);

  select.addEventListener('change', (e) => {
    setLanguage(e.target.value);
  });
});
